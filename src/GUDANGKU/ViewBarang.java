    private void initTabelBarang(){
    String[] obatColums = new String[]{"ID", "Nama Barang","Harga", "Ukuran" , "Stok"};
        int[] columnWidth = {
        70,500,200,200,200
        };
        
        tableModel = new DefaultTableModel(obatColums,0);
        BarangTabel.setModel(tableModel);
        BarangTabel.setRowHeight(20);
        
        int i = 0;
        for(int width : columnWidth){
            TableColumn column = BarangTabel.getColumnModel().getColumn(i++);
            column.setMaxWidth(width);
            column.setMaxWidth(width);
            column.setPreferredWidth(width);
        }
    }
    
     public void datatable(String nama) {
         DefaultTableModel model = (DefaultTableModel) BarangTabel.getModel();
        try{
            model.setRowCount(0);
            java.sql.Statement statement = (java.sql.Statement)Koneksi.GetConnection().createStatement();
            ResultSet rs = statement.executeQuery("SELECT * FROM barang WHERE nama_barang Like '%" + nama + "%'");
            while (rs.next()){
              Object[] data = new Object[]{
                rs.getString(1),
                rs.getString(2),
                rs.getString(3),
                rs.getString(4),
                rs.getString(5)
              };
              model.addRow(data);
            }
        }catch (Exception e){
            System.out.println("Error Data Obat : " + e);
            model.setRowCount(5);
        }
    }

