    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        try {
            int baris = BarangTabel.getSelectedRow();
            String id = (String) BarangTabel.getValueAt(baris, 0);
            
            java.sql.Statement statement = (java.sql.Statement)Koneksi.GetConnection().createStatement();
            ResultSet rs = statement.executeQuery("SELECT * FROM barang WHERE id_barang ='" + id + "'");
            
            if(rs.next()){
                int konfirmasi = JOptionPane.showConfirmDialog(this, "Anda yakin hapus id : " + id + "?", 
                        "Konfirmasi", JOptionPane.YES_NO_OPTION);
                
                if(konfirmasi == JOptionPane.YES_NO_OPTION){
                   statement.executeUpdate("DELETE FROM barang WHERE id_barang ='" + id + "'");
                   datatable("");
                }
        }
            
        }catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Tidak ada data yang dipilih", "Peringatan", JOptionPane.WARNING_MESSAGE);
            System.out.println("Error hapus barang " + e);
        }
    }//GEN-LAST:event_btnhapusActionPerformed

